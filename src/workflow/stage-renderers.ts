function renderDebug(input: GenerateArtifactInput): string {
  return [
    "# Debug 收敛",
    "",
    "## 失败现象",
    "- 若 npm run generate:demo 失败，先记录命令输出、退出码和缺失文件。",
    "- 若 smoke 失败，记录模块类型、测试租户、configId、requestId、下游 RPC 错误和超时配置。",
    "- 当前任务：",
    formatTask(input),
    "",
    "## 首查路径",
    numberedList(
      [
        "先看本地命令输出，确认是 typecheck、test、文件读取还是写入失败。",
        "再看 trace.json 是否缺少 contextUsed、previousArtifactsUsed 或 outputArtifactId。",
        "产品 smoke 失败时，先用 requestId 关联发布入口、配置快照发布和下游 RPC。",
        "检查测试租户权限、配置状态、配置中心超时和下游 mock / 真实环境差异。",
      ],
      "暂无首查路径。",
    ),
    "",
    "## 证据来源",
    "- 本地：npm test、npm run typecheck、npm run generate:demo、本地 generated/demo-task/trace.json。",
    "- 产品：日志平台、下游 RPC request/response、配置中心、测试租户和配置数据。",
    "- 输入 context：",
    formatContextFiles(input.contextFiles),
    "",
    "## 排查顺序",
    numberedList(
      [
        "确认失败是否可复现，并保留完整命令或 requestId。",
        "确认输入 context、前序 artifact 和 human notes 是否进入 trace。",
        "确认发布入口是否命中预期模块类型和配置模型。",
        "确认下游 RPC、配置和租户权限是否与 smoke case 匹配。",
        "修复后重新跑最小失败 case，再跑完整验证闭环。",
      ],
      "暂无排查顺序。",
    ),
    "",
    "## 可能根因",
    "- 模板或 stage spec heading 与 workspace 约定不一致。",
    "- demo context 路径不在当前 cwd 下，导致 contextUsed 为空或读取失败。",
    "- 产品 smoke 中下游 RPC 超时、测试租户无权限、mock 数据与真实链路不一致。",
    "- 发布逻辑只覆盖单一应用模块，未处理多模块差异字段。",
    "",
    "## 修复后验证",
    "- 本地修复后重新运行 typecheck、test 和 generate:demo。",
    "- 产品修复后用同一测试租户和配置参数复跑 smoke，并保留日志证据。",
    "- 如果根因改变需求边界，回写 requirement_orientation 或 technical_design artifact。",
    "",
    "人工 notes：",
    formatHumanNotes(input.humanNotes),
    "",
    "前序 artifact：",
    formatPreviousArtifacts(input.previousArtifacts),
  ].join("\n");
}

function renderAssetFeedback(input: GenerateArtifactInput): string {
  return [
    "# 资产回流",
    "",
    "## Context Patch 建议",
    "- 将多应用模块发布链路中的共用入口、差异字段和下游依赖补充到 domain context。",
    "- 将配置快照字段、幂等策略、事务边界和回显路径补充到 repo context。",
    "- 将验证失败时高信号证据来源补充到 repo-for-debug。",
    "- 输入 context：",
    formatContextFiles(input.contextFiles),
    "",
    "## Skill / Workflow Patch 建议",
    "- 在 workflow distillation 流程中保留阶段化 artifact、trace 和人工 checkpoint。",
    "- 对发布链路改造类任务，要求先完成 requirement_orientation 与 system_modeling，再进入 technical_design。",
    "- 对 smoke 失败类任务，要求 debug artifact 明确日志、配置、测试租户和下游 RPC 的排查顺序。",
    "",
    "## Eval Case 建议",
    "- 构造多应用模块共享发布入口但字段差异不同的 case，评估是否能输出正确 coding plan。",
    "- 构造下游 RPC 超时或测试租户无权限的 smoke 失败 case，评估 debug 收敛质量。",
    "- 构造需求边界不清的 PRD case，评估 requirement_orientation 是否能拆出待确认问题。",
    "",
    "## 不应沉淀的内容",
    "- 单次测试租户、一次性 requestId、临时 mock 地址和仅在当前环境成立的配置值。",
    "- 未经过代码、日志或 reviewer 确认的产品猜测。",
    "- 与配置快照发布无关的应用模块泛化结论。",
    "",
    "## 需要人工审核的内容",
    "- 是否把一次性故障错误地写成通用 debug 规则。",
    "- context patch 是否包含敏感业务细节或不可公开的环境信息。",
    "- skill / workflow patch 是否真的复用，还是只适合本次任务。",
    "- 前序 artifact：",
    formatPreviousArtifacts(input.previousArtifacts),
    "- 人工 notes：",
    formatHumanNotes(input.humanNotes),
  ].join("\n");
}
