import type { WorkflowStage } from "../contracts/artifact.js";
import type { GenerateArtifactInput } from "./types.js";
import {
  bulletList,
  formatContextFiles,
  formatHumanNotes,
  formatPreviousArtifacts,
  formatTask,
  numberedList,
} from "./markdown-helpers.js";

type StageRenderer = (input: GenerateArtifactInput) => string;

const stageRenderers: Record<WorkflowStage, StageRenderer> = {
  requirement_orientation: renderRequirementOrientation,
  system_modeling: renderSystemModeling,
  technical_design: renderTechnicalDesign,
  coding_plan: renderCodingPlan,
  verification: renderVerification,
  debug: renderDebug,
  asset_feedback: renderAssetFeedback,
};

export function renderArtifactMarkdown(input: GenerateArtifactInput): string {
  return `${stageRenderers[input.stage](input).trim()}\n`;
}

function renderRequirementOrientation(input: GenerateArtifactInput): string {
  return [
    "# 需求定向",
    "",
    "## 本期目标",
    formatTask(input),
    "- 明确本期研发任务要解决的发布链路问题，并形成后续阶段可复用的范围边界。",
    "",
    "## 做什么",
    "- 从 PRD、评审记录、技术方案和 domain context 中提取本期要覆盖的产品链路。",
    "- 区分多个应用模块的差异点、共性路径和配置快照发布的复用机会。",
    "- 输出后续现状建模、技术方案、coding plan 和验证闭环需要使用的问题清单。",
    "",
    "## 不做什么",
    "- 不在本阶段直接修改产品代码或做完整自动化 agent 编排。",
    "- 不把尚未回到代码、日志或配置确认的假设写成确定结论。",
    "- 不扩展复杂 UI、eval dashboard 或真实外部 LLM 调用。",
    "",
    "## 关键产品问题",
    "- 哪些应用模块需要发布配置快照，哪些只需要透传或校验配置结果？",
    "- 多应用模块是否共享同一发布入口、同一数据模型和同一幂等策略？",
    "- 配置快照失败、超时或下游不可用时，产品侧应该降级、重试还是阻断发布？",
    "- 输入 context：",
    formatContextFiles(input.contextFiles),
    "",
    "## 待确认问题",
    "- 当前需求是否只覆盖配置快照发布，还是也包含配置快照生成、回填和校验？",
    "- 不同应用模块的发布链路差异是否来自产品规则、配置模型还是下游 RPC 能力？",
    "- smoke case 需要覆盖哪些模块、租户、配置类型和异常分支？",
    "",
    "## 人工确认点",
    formatHumanNotes(input.humanNotes),
  ].join("\n");
}

function renderSystemModeling(input: GenerateArtifactInput): string {
  return [
    "# 现状建模",
    "",
    "## 当前链路",
    formatTask(input),
    "- 从页面或上游任务进入应用模块发布接口后，发布服务会读取配置参数、配置上下文和模块类型。",
    "- 配置快照发布链路应区分配置主数据发布、配置快照发布、下游 RPC 写入和异步补偿。",
    "- 前序 artifact：",
    formatPreviousArtifacts(input.previousArtifacts),
    "",
    "## 关键入口",
    "- 发布入口：应用模块发布 API、配置发布 service、配置快照发布 helper。",
    "- 查询入口：配置详情、配置快照回显、校验结果或状态看板相关读取路径。",
    "- 失败入口：smoke case、日志 requestId、下游 RPC error、超时配置和测试租户权限。",
    "",
    "## 相关 repo / 模块",
    "- domain context：识别产品域链路、模块差异和上下游服务边界。",
    "- repo-code-map：定位 handler、service、model、RPC client 和发布落点。",
    "- repo-for-codegen：指导 coding 阶段优先阅读的文件和改动顺序。",
    "- repo-for-debug：指导验证失败时回到日志、配置、测试租户和下游 RPC。",
    "- 输入 context：",
    formatContextFiles(input.contextFiles),
    "",
    "## 上下游依赖",
    "- 上游：PRD、客户端提交参数、配置评估/校验结果、配置草稿。",
    "- 本服务：发布入口、配置模型转换、配置快照发布、幂等和事务边界。",
    "- 下游：配置快照服务、配置发布 RPC、配置中心、日志平台和效果回显链路。",
    "",
    "## 当前系统约束",
    "- 多模块发布链路可能存在字段粒度、入口复用和事务一致性差异。",
    "- 下游 RPC 超时、租户权限和测试数据状态会直接影响 smoke 结果。",
    "- 当前 MVP 只建模 artifact 生成，不自动读取真实仓库或调用外部平台。",
    "",
    "## 仍需回到代码确认的问题",
    bulletList(
      [
        "不同应用模块当前是否共用发布 handler 和 service。",
        "配置快照字段是随配置主数据一起落库，还是独立表 / 独立 RPC 写入。",
        "发布失败时现有代码是否已有重试、降级、事务回滚或补偿机制。",
        "日志中能否稳定关联 taskId、configId、moduleType 和下游 RPC requestId。",
      ],
      "暂无。",
    ),
    "",
    "人工 notes：",
    formatHumanNotes(input.humanNotes),
  ].join("\n");
}

function renderTechnicalDesign(input: GenerateArtifactInput): string {
  return [
    "# 技术方案",
    "",
    "## 背景",
    formatTask(input),
    "- 本任务需要在多个应用模块发布链路中统一配置快照发布口径，同时保留模块差异。",
    "- 方案必须能被 review、comment，并继续交给 agent 修改。",
    "",
    "## 目标",
    "- 明确配置快照发布的复用路径、模块差异和责任边界。",
    "- 形成对代码改动顺序、验证路径和失败收敛方式有直接指导意义的方案。",
    "- 保持 MVP 范围：只生成 Markdown artifact 和 trace，不引入 runtime 编排或真实 LLM 调用。",
    "",
    "## 方案概述",
    "- 以 workflow stage spec 作为阶段定义 source of truth。",
    "- 使用模板生成稳定 Markdown artifact，后续可替换为 LLM renderer。",
    "- 每次生成 artifact 同步产出 trace，记录 context、前序 artifact、人工 notes 和输出 id。",
    "",
    "## 详细设计",
    numberedList(
      [
        "合同层复用 Artifact contract，统一 stage、title、markdown 和 metadata。",
        "Stage spec 层定义 title、description、expectedInputs、expectedOutputs、humanCheckpoint 和 promptTemplate。",
        "Generator 层接收 task、stage、contextFiles、previousArtifacts 和 humanNotes，调用 renderer 生成 markdown。",
        "Trace 层为每个阶段记录输入摘要、contextUsed、previousArtifactsUsed、humanNotesUsed 和 outputArtifactId。",
        "Demo 层读取内置 demo context，按固定阶段顺序生成 7 个 Markdown 文件和 trace.json。",
      ],
      "暂无详细设计。",
    ),
    "",
    "## 影响面",
    "- 新增 TypeScript workflow 模块、demo script、内置 demo context、本地 demo 产物和 README。",
    "- 不修改无关代码，不引入重依赖，不接外部 LLM、真实仓库读取或复杂 UI。",
    "- 后续 Dorey 可直接加载本地生成的 demo Markdown 文件。",
    "",
    "## 风险与兼容性",
    "- 模板生成不会理解真实代码语义，必须通过人工 review 和后续 LLM 接入补强。",
    "- 如果后续 Artifact contract 扩展字段，需要保持当前最小字段向后兼容。",
    "- trace.json 是 demo 级聚合格式，后续 runtime 化前不承担状态机职责。",
    "",
    "## 验证方案",
    "- typecheck 确认 TypeScript contract、stage spec 和 generator API 可编译。",
    "- unit test 确认 7 个 stage、promptTemplate、Markdown heading、trace 和 demo 文件名稳定。",
    "- 运行 npm run generate:demo，检查本地 demo 产物可直接 review。",
    "",
    "## 人工决策点",
    formatHumanNotes(input.humanNotes),
    "",
    "参考 context：",
    formatContextFiles(input.contextFiles),
    "",
    "前序 artifact：",
    formatPreviousArtifacts(input.previousArtifacts),
  ].join("\n");
}

